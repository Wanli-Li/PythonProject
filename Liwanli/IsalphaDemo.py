mystr1 = "hello"
mystr2 = "12345"

# isalpha() 是否全字母
print(mystr1.isalpha())
print(mystr2.isalpha())

# isdigit() 是否全数字
print(mystr1.isdigit())
print(mystr2.isdigit())

# isalnum（） 是否全字母数字组合
mystr3 = "hello-12345"
print(mystr1.isalnum())
print(mystr2.isalnum())
print(mystr3.isalnum())

# isspace() 是否空白
mystr4 = "hello world"
print(mystr4.isspace())
